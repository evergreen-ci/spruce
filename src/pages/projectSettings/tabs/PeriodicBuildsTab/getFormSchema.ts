import widgets from "components/SpruceForm/Widgets";
import { timeZones } from "constants/fieldMaps";
import { GetFormSchema } from "../types";
import { form, ProjectType } from "../utils";

const { overrideRadioBox } = form;

export const getFormSchema = (
  projectType: ProjectType,
  timezone: string
): ReturnType<GetFormSchema> => ({
  fields: {},
  schema: {
    type: "object" as "object",
    description: "Configure tasks to run at fixed intervals.",
    ...overrideRadioBox(
      "periodicBuilds",
      ["Override Repo Commands", "Default to Repo Commands"],
      {
        type: "array" as "array",
        default: [],
        items: {
          type: "object" as "object",
          properties: {
            intervalHours: {
              type: "number" as "number",
              title: "Interval",
              minimum: 1,
              default: 24,
            },
            configFile: {
              type: "string" as "string",
              title: "Config File",
              minLength: 1,
              default: "",
            },
            alias: {
              type: "string" as "string",
              title: "Alias",
              default: "",
            },
            message: {
              type: "string" as "string",
              title: "Message",
              default: "",
            },
            timezone: {
              type: "string" as "string",
              title: "Time Zone",
              default: timezone,
              oneOf: [
                ...timeZones.map(({ str, value }) => ({
                  type: "string" as "string",
                  title: str,
                  enum: [value],
                })),
                {
                  type: "string" as "string",
                  title: "Local Time",
                  enum: [""],
                },
              ],
            },
            nextRunTime: {
              type: "string" as "string",
              title: "Next Run Time",
              default: new Date().toString(),
            },
          },
          required: ["intervalHours"],
        },
      }
    ),
  },
  uiSchema: {
    periodicBuildsOverride: {
      "ui:widget":
        projectType === ProjectType.AttachedProject
          ? widgets.RadioBoxWidget
          : "hidden",
      "ui:showLabel": false,
    },
    periodicBuilds: {
      "ui:addButtonText": "Add Periodic Build",
      "ui:orderable": false,
      "ui:showLabel": false,
      "ui:useExpandableCard": true,
      items: {
        "ui:displayTitle": "New Periodic Build",
        intervalHours: {
          "ui:data-cy": "interval-input",
          "ui:description": "Number of hours between runs.",
        },
        configFile: {
          "ui:data-cy": "config-file-input",
          "ui:placeholder": ".evergreen.yml",
        },
        alias: {
          "ui:optional": true,
          "ui:placeholder": "my_task_alias",
        },
        message: {
          "ui:optional": true,
          "ui:placeholder": "A periodic build",
        },
        timezone: {
          "ui:description":
            "Next Run Time is configured using the time zone set in User Preferences.",
          "ui:disabled": true,
        },
        nextRunTime: {
          "ui:disableBefore": new Date(),
          "ui:widget": "date-time",
        },
      },
    },
    repoData: {
      "ui:orderable": false,
      "ui:readonly": true,
      periodicBuilds: {
        "ui:showLabel": false,
        "ui:useExpandableCard": true,
        items: {
          nextRunTime: {
            "ui:widget": "date-time",
          },
        },
      },
    },
  },
});
